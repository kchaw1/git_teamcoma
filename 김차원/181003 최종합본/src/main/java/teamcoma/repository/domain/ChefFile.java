package teamcoma.repository.domain;

public class ChefFile {
	private int no;
	private int classNo;
	private int chefFileNo;
	private String chefFileName;
	private String chefFilePath;
	private String chefFileClassify;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getClassNo() {
		return classNo;
	}
	public void setClassNo(int classNo) {
		this.classNo = classNo;
	}
	public int getChefFileNo() {
		return chefFileNo;
	}
	public void setChefFileNo(int chefFileNo) {
		this.chefFileNo = chefFileNo;
	}
	public String getChefFileName() {
		return chefFileName;
	}
	public void setChefFileName(String chefFileName) {
		this.chefFileName = chefFileName;
	}
	public String getChefFilePath() {
		return chefFilePath;
	}
	public void setChefFilePath(String chefFilePath) {
		this.chefFilePath = chefFilePath;
	}
	public String getChefFileClassify() {
		return chefFileClassify;
	}
	public void setChefFileClassify(String chefFileClassify) {
		this.chefFileClassify = chefFileClassify;
	}
	
	
}
